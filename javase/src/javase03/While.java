package javase03;

public class While {
    public static void main(String[] args) {
        /*
        while
        循环变量初始化
        while（循环条件）{
            循环体（语句）；
            循环变量送代：
        }
         */
        /*
        do... while...
        循环变量初始化 ；
        do{
            循环体 （ 语句 ） ；
            循环变量迭代 ；
        } while （ 循环条件 ） ；

        */
        /*
        ． 说明 ：
        1 · do while 是关键字
        1 · 也有循环四要素 ， 只是位置不一样
        2 · 先执行 ， 再判断 ， 也就是说 ， 一定会执行一
        3 · 最后有一个分号 ；
        4 · while 和 do..while 区别举例 ： 要账
        5 . 循环嵌套的循环次数 = 内层循环 * 外层循环
         */
    }
}
